    public void shImg() {
        try {
            String sql = "SELECT * FROM tiket WHERE idTiket = '"+idT+"'";
            java.sql.Connection conn = ((Connection) Config.configDB());
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet res = stm.executeQuery(sql);
            if (res.next()) {
                byte [] img = res.getBytes("poster");          
                artisL.setText("Artis: " + res.getString("artis"));
                temaL.setText("Tema: " + res.getString("tema"));
                tglL.setText("Tanggal: " + res.getString("tanggal"));
                lokasiL.setText("Lokasi: " + res.getString("lokasi"));
                pukukL.setText("Pukul: " + res.getString("pukul"));
                 hargaL.setText("harga: Rp" + res.getString("harga"));
                ImageIcon image = new ImageIcon(img);
                Image im = image.getImage();
                Image myImg = im.getScaledInstance(300, 300,Image.SCALE_SMOOTH);
                ImageIcon icon = new ImageIcon(myImg);
                posterL.setIcon(icon);      
                deskTa.setLineWrap(true);
                DefaultCaret caret = (DefaultCaret) deskTa.getCaret();
                caret.setUpdatePolicy(DefaultCaret.NEVER_UPDATE);
                deskTa.setText(res.getString("deskripsi"));
                int kuota = Integer.parseInt(res.getString("kuota"));
                if (kuota > 0 ){                    
                    kuotaL.setText("Kuota: " + res.getString("kuota") + " Orang");                   
                } else {
                    kuotaL.setText("Kuota penuh");
                }                                                          
            } else
                JOptionPane.showMessageDialog(null, "No Data");
        } catch(Exception e)  {
            e.printStackTrace();
        }
    }
