    private void beliBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_beliBtnActionPerformed
        // TODO add your handling code here:
        try {
            String sql = "SELECT kuota FROM tiket WHERE idTiket = '"+idT+"'";
            java.sql.Connection conn = ((Connection) Config.configDB());
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet res = stm.executeQuery(sql);
            int kuota = 0;
            if (res.next()) 
                 kuota = Integer.parseInt(res.getString("kuota"));
            if (kuota > 0 ){
                this.dispose();
                new pembayaran().setVisible(true);
            } else {
                 JOptionPane.showMessageDialog(null, "Kuota Penuh");
            }          
        } catch(Exception e) {
            JOptionPane.showMessageDialog(null, "Ada error"
            + e.getMessage());
        }
    }//GEN-LAST:event_beliBtnActionPerformed

